// Function to toggle dark mode
function toggleDarkMode() {
  const profilePic = document.querySelector('.profile-pic');

  // Add a "glitch" effect
  document.body.classList.add('glitch');

  // If a profile picture exists, temporarily change it
  if (profilePic) {
    profilePic.src = 'assets/pepe_cook.png';
  }

  setTimeout(() => {
    // If a profile picture exists, revert it
    if (profilePic) {
      profilePic.src = 'assets/ben_profile_pic.png';
    }

    // Toggle the dark mode class
    document.body.classList.toggle('dark-mode');
    document.body.classList.remove('glitch');

    // Store the user's choice in localStorage
    const isDarkMode = document.body.classList.contains('dark-mode');
    localStorage.setItem('darkMode', isDarkMode ? 'true' : 'false');

    // Update the button icon dynamically
    updateButtonIcon(isDarkMode);
  }, 200); // Delay for the flash effect
}

